    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if(select>=5){
            String uname=jTextField1.getText().toLowerCase();
           try{
            
                Class.forName("java.sql.DriverManager");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/graphicalpwd","root","1234");
                Statement st=con.createStatement();
                String query;
                query="Select phone from form where uname='"+uname+"';";
                ResultSet rs=st.executeQuery(query);
                while(rs.next()){
                    phno=rs.getLong("phone");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error"+e);
            }
           ldigit=(int)(phno%10);
           try{
            
                Class.forName("java.sql.DriverManager");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/graphicalpwd","root","1234");
                Statement st=con.createStatement();
                String query;
                query="Select pwd from form where uname='"+uname+"';";
                ResultSet rs=st.executeQuery(query);
                while(rs.next()){
                    cpwd=rs.getString("pwd");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error"+e);
            }
            for(int i=0;i<cpwd.length();i++){
                cipher+=(char)(cpwd.charAt(i)-ldigit);
            }
            if(pwd.equals(cipher)==true){
                JOptionPane.showMessageDialog(this,"You have successfully logged in!");
                new Account(uname).setVisible(true);
                setVisible(false); 
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this,"Incorrect Password!");
                this.setVisible(false);
                new MainPage().setVisible(true);
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Your Graphical Password Pattern is less than 5. It should be equal to or more than 5");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

